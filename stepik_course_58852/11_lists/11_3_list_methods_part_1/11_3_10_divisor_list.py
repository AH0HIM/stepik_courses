"""
Список делителей
На вход программе подается натуральное число nn. Напишите программу, которая создает список состоящий из делителей
введенного числа.

Формат входных данных
На вход программе подается натуральное число nn.

Формат выходных данных
Программа должна вывести список, состоящий из делителей введенного числа.

https://stepik.org/lesson/327207/step/10?unit=310501
"""

n = int(input())
lst = []

for i in range(1, n + 1):
    if n % i == 0:
        lst.append(i)
print(lst)
