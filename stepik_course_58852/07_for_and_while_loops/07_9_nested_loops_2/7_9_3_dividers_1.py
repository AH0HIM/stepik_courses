a, b = int(input()), int(input())

total_divider = 0
max_divider = 0
counter = 0

for i in range(a, b + 1):
    for j in range(1, i + 1):
        if i % j == 0:
            counter += j
    if counter >= total_divider:
        total_divider = counter
        max_divider = i
    counter = 0

print(max_divider, total_divider)