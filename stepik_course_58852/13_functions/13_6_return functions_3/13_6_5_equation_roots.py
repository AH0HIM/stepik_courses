from math import *


def solve(a, b, c):
    d = pow(b, 2) - (4 * (a * c))
    x1 = (-b - sqrt(d)) / (2 * a)
    x2 = (-b + sqrt(d)) / (2 * a)

    return min(x1, x2), max(x1, x2)


