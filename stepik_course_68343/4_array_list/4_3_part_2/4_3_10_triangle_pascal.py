def pascal(num):
    my_list = []
    for i in range(num + 1):
        row = [1] * (i + 1)
        for j in range(i + 1):
            if j != 0 and j != i:
                row[j] = my_list[i - 1][j - 1] + my_list[i - 1][j]
        my_list.append(row)

    print(my_list[num])


